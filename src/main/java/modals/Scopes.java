package modals;

import java.util.Arrays;

public class Scopes {

	String[] scopes;

	public String[] getScopes() {
		return scopes;
	}

	public void setScopes(String[] scopes) {
		this.scopes = scopes;
	}

	@Override
	public String toString() {
		return "Scopes [scopes=" + Arrays.toString(scopes) + "]";
	}
	
	
	
}
